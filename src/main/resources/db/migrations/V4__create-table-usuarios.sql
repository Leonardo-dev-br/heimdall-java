CREATE TABLE TB_HDL_USERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100),
    MIDDLENAME VARCHAR2(100),
    BIRTHDAY DATE,
    CPF VARCHAR2(14),
    EMAIL VARCHAR2(150),
    PASSWORD VARCHAR2(100),
    CATEGORY_ID NUMBER,
    CONSTRAINT FK_USER_CATEGORY FOREIGN KEY (CATEGORY_ID)
