CREATE TABLE TB_HDL_TAG_IDENTIFICACAO (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    ID_MOTO NUMBER,
    FREQUENCIA VARCHAR2(255 CHAR) NOT NULL,
    BANDA VARCHAR2(255 CHAR) NOT NULL,
    APLICACAO VARCHAR2(255 CHAR) NOT NULL,
    MOTORCYCLE_ID NUMBER UNIQUE,
    CONSTRAINT FK_TAG_MOTORCYCLE FOREIGN KEY (MOTORCYCLE_ID)
        REFERENCES TB_HDL_MOTO(ID)
);
